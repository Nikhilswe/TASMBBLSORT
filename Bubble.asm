CODE SEGMENT 
ASSUME CS:CODE,DS:DATA
START : MOV AX,DATA
	  MOV DS,AX
	  MOV CH,B
AGN1  : MOV CL,CH
	  MOV SI,OFFSET A
REPT  : MOV AL,[SI]
	  INC SI
	  CMP AL,[SI]
	  JBE NEXT
	  XCHG AL,[SI]
	  MOV [SI-1],AL
NEXT  : DEC CL
	  JNZ REPT
	  DEC CH
	  JNZ AGN1
	  INT 3
CODE ENDS
END START	


